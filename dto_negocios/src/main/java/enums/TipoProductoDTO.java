package enums;

/**
 * TipoProductoDTO.
 * Representa los tipos de productos disponibles en el sistema.
 *
 * Este enum se utiliza para clasificar los productos del menu
 * de acuerdo a su categoria.
 *
 * @author DishUp
 */
public enum TipoProductoDTO {

    /**
     * Productos de tipo bebida.
     */
    BEBIDA,

    /**
     * Productos de tipo comida principal.
     */
    COMIDA,

    /**
     * Productos de tipo botana o aperitivo.
     */
    BOTANA
}
