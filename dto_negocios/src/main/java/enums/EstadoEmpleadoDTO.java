package enums;

/**
 * EstadoEmpleadoDTO.
 * Representa los posibles estados de un empleado dentro del sistema.
 *
 * Este enum se utiliza para indicar si un empleado
 * se encuentra disponible o no dentro del sistema.
 *
 * @author DishUp
 */
public enum EstadoEmpleadoDTO {
    
    /**
     * El empleado se encuentra activo dentro del sistema.
     */
    ACTIVO,
    
    /**
     * El empleado se encuentra inactivo dentro del sistema.
     */
    INACTIVO
}
