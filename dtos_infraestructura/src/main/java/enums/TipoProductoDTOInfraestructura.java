package enums;

/**
 * TipoProductoDTOInfraestructura.
 * 
 * Representa los tipos de producto utilizados dentro de la capa
 * de infraestructura para clasificar los productos que se manejan
 * en los sistemas externos.
 * 
 * Este enum permite estandarizar la comunicacion entre el sistema
 * interno y la infraestructura, definiendo categorias basicas
 * como bebidas, comida y botanas.
 * 
 * @author DishUp
 */
public enum TipoProductoDTOInfraestructura {
    BEBIDA,
    COMIDA,
    BOTANA
}